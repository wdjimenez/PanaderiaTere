    private void llenarComboBox() {        
        comboUsuarios.addItem(new ComboItemUser("Seleccionar usuario", "", "", 0));
        
        Usuario.allUsers().forEach((Usuario u) -> {
            comboUsuarios.addItem(new ComboItemUser(u.getUser(), u.getNombre(), u.getApellido(), 1));
        });        
    }    
