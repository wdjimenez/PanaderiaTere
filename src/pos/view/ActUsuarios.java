    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String myPass1 = String.valueOf(passnew.getPassword());
        String myPass2 = String.valueOf(passrep.getPassword());
        
        //Recuperamos el id seleccionado
        ComboItemUser item = (ComboItemUser)comboUsuarios.getSelectedItem(); 
        
        if(item.getValid() == 0){ //falso usuario
            JOptionPane.showMessageDialog(this, "Seleccione un usuario de la lista para actualizar");
            return;
        }                
        
        Usuario user = Usuario.find(item.getUser());
        
        if(myPass1.compareTo("") == 0){
            JOptionPane.showMessageDialog(this, "El password no puede ser vacio");
            return;
        }
        if(myPass1.compareTo(myPass2) != 0){
            JOptionPane.showMessageDialog(this, "Las contraseñas no coinciden");
            return;
        }
        
        //Recuperamos la semilla
        //String salt = PasswordUtils.getSalt(30);        
        String newPass = PasswordUtils.generateSecurePassword(myPass1, user.getSalt());
        
        if(Usuario.updatePass(user.getUser(), newPass, user.getSalt())){
            JOptionPane.showMessageDialog(this, "Se actualizó la contraseña correctamente");
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(this, "Se presento un problema al actualizar la contraseña");
        }        
    }//GEN-LAST:event_jButton1ActionPerformed

